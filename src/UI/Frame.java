    private void contraseñaLoginMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_contraseñaLoginMouseEntered
        // TODO add your handling code here:
        char contra[] = contraseñaLogin.getPassword();
        char compare[] = ("xxxxxxxx").toCharArray();
        int total_vueltas=0;
        int total_compar=0;

        if (contra.length<compare.length) {
            total_vueltas=contra.length;
        }else if(compare.length<contra.length){
            total_vueltas=compare.length;
        }else{
            total_vueltas=compare.length;
        }


        for (int i = 0; i < total_vueltas; i++) {
            if (contra[i]==compare[i]) {
                total_compar++;
            }
        }
        if (total_compar==total_vueltas) {
            contraseñaLogin.setText("");
        }
        
    }//GEN-LAST:event_contraseñaLoginMouseEntered


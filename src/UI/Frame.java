    private void guardarRegistraseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarRegistraseActionPerformed
        // TODO add your handling code here
        // Usuario2 user = new Usuario2();

        if (!(C_CRegistrarse.getText().equals("")&&TelefonoRegistrarse.getText().equals("")&&DireccionRegistrarse.getText().equals("")&&emailRegistrarse.getText().equals("")&&usuarioRegistrarse.getText().equals("")&&edadRegistrarse.getText().equals(""))) {
            if (!Usuario.usuarios().contains(usuarioRegistrarse.getText())) {
                user = new Usuario();
                user.setNombre_completo(nombreCompletoRegistrarse.getText());
                user.setEdad(Integer.parseInt(edadRegistrarse.getText()));
                user.setcC(Long.parseLong(C_CRegistrarse.getText()));
                user.setTelefono(Long.parseLong(TelefonoRegistrarse.getText()));
                user.setDireccion(DireccionRegistrarse.getText());
                user.setEmail(emailRegistrarse.getText());
                user.setUsuario(usuarioRegistrarse.getText());
                user.setContraseña(contraseñaRegistrarse.getPassword());
                user.guardar();
                
                nombre_pg1.setText(user.getNombre_completo());
                Saldo_pg1.setText(user.getSaldoFormat());
                
                fondoRegistro.setVisible(false);
                fondoInicio.setVisible(true);
                
            } else {
                alerta_registro.setForeground(Color.RED);
                alerta_registro.setText("El usuario ya existe");
    
            }
        }else{
            
            alerta_registro.setForeground(Color.RED);
            alerta_registro.setText("Faltan campos por llenar");
        }
    }//GEN-LAST:event_guardarRegistraseActionPerformed

